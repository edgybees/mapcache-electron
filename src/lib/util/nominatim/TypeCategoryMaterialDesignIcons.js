function getMaterialDesignIcon (type, category) {
  let icon = null
  if (type != null && category != null && categoryTypeIconMap[category.toLowerCase()] && categoryTypeIconMap[category.toLowerCase()][type.toLowerCase()]) {
    icon = categoryTypeIconMap[category.toLowerCase()][type.toLowerCase()]
  }
  return icon
}
